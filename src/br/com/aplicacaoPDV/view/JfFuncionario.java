    private void jbPesqNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesqNomeActionPerformed
        // Botão pesquisar
        String nome = "%" + jtfPesquisa.getText() + "%";

        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionarios> lista = dao.buscaFuncionairoPorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) tabelaFuncionarios.getModel();
        dados.setNumRows(0); //limpa a tabela de clientes

        for (Funcionarios c : lista) {
            dados.addRow(new Object[]{ // Adiciona uma linha na tabela com dados do cliente passados no array Object
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                
                c.getSenha(),
                c.getCargo(),
                c.getNivelAcesso(),
                
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getUf()

            });
        }

    }//GEN-LAST:event_jbPesqNomeActionPerformed

