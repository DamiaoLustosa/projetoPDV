    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // Botão buscar cliente por nome

        String nome = jtfNome.getText();
        Funcionarios obj = new Funcionarios();
        FuncionarioDAO dao = new FuncionarioDAO();

        obj = dao.consultaPorNome(nome);

        if (obj.getNome() != null) {

            //Exibir os dados do cliente nos campos de texto
            jtfCodigo.setText(String.valueOf(obj.getId()));
            jtfNome.setText(obj.getNome());
            jtfRg.setText(obj.getRg());
            jtfCpf.setText(obj.getCpf());
            jtfEmail.setText(obj.getEmail());
            
            jtfSenha.setText(obj.getSenha());
            jtfCargo.setText(obj.getCargo());
            jcbNivelAcesso.setSelectedItem(obj.getNivelAcesso());
              
            jtfTelefone.setText(obj.getTelefone());
            jtfCelular.setText(obj.getCelular());
            jtfCep.setText(obj.getCep());
            jtfEndereco.setText(obj.getEndereco());
            jtfNum.setText(String.valueOf(obj.getNumero()));
            jtfComplemento.setText(obj.getComplemento());
            jtfBairro.setText(obj.getBairro());
            jtfCidade.setText(obj.getCidade());
            jcbUf.setSelectedItem(obj.getUf());
        } else {
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado!!");
            new Utilitarios().limpaTela(painelDados);
        }


    }//GEN-LAST:event_jbBuscarActionPerformed

